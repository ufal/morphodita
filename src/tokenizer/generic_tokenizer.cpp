	break;
	case 12:
	{te = ( text);( text)--;{ forms.emplace_back(ts, te - ts);

          if (emergency_sentence_split(forms)) {( text)++; goto _out; }
        }}
	break;
	case 9:
	{te = ( text);( text)--;}
	break;
	case 10:
	{te = ( text);( text)--;{ if (!forms.empty()) {( text)++; goto _out; } }}
